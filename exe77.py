pala = ('amo', 'muito', 'fran', 'familia', 'felicidade', 'amor')
for c in pala:
    print(f'\nNa palavra {c} temos', end = ' ')
    if 'a' in c:
        print('a', end = ' ')
    if 'e' in c:
        print('e', end = ' ')
    if 'i' in c:
        print('i', end = ' ')
    if 'o' in c:
        print('o', end = ' ')
    if 'u' in c:
        print('u', end = ' ')
    